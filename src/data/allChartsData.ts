// Функция для получения данных по chartId и dataKey
export function getChartData(chartId: string, dataKey?: string): any[] | null {
  const module = allChartsDataModules[chartId];
  if (!module) {
    console.error(`[AllData] Модуль не найден для chartId: ${chartId}`);
    console.error(`[AllData] Доступные chartId (первые 10):`, Object.keys(allChartsDataModules).slice(0, 10));
    return null;
  }
  
  // Если указан dataKey, ищем по нему (точное совпадение)
  if (dataKey && module[dataKey]) {
    return module[dataKey];
  }
  
  // Если dataKey указан, но не найден точно, пробуем найти по частичному совпадению
  if (dataKey) {
    const keys = Object.keys(module);
    // Сначала пробуем точное совпадение (case-insensitive)
    let matchingKey = keys.find(key => key.toLowerCase() === dataKey.toLowerCase());
    
    // Если не нашли, пробуем частичное совпадение
    if (!matchingKey) {
      matchingKey = keys.find(key => 
        key.toLowerCase().includes(dataKey.toLowerCase()) || 
        dataKey.toLowerCase().includes(key.toLowerCase()) ||
        key.toLowerCase().replace(/[^a-z0-9]/g, '').includes(dataKey.toLowerCase().replace(/[^a-z0-9]/g, ''))
      );
    }
    
    // Если не нашли, пробуем найти ключ, содержащий "Data"
    if (!matchingKey) {
      matchingKey = keys.find(key => 
        key.toLowerCase().includes('data') && Array.isArray(module[key])
      );
    }
    
    if (matchingKey && Array.isArray(module[matchingKey])) {
      const data = module[matchingKey];
      if (data.length > 0) {
        return data;
      }
    }
  }
  
  // Ищем первый массив в модуле, который содержит данные графика
  const keys = Object.keys(module);
  for (const key of keys) {
    // Пропускаем служебные ключи
    if (key === 'default' || key.startsWith('_')) continue;
    
    const value = module[key];
    if (Array.isArray(value) && value.length > 0) {
      const firstItem = value[0];
      if (firstItem && typeof firstItem === 'object' && ('year' in firstItem || 'value' in firstItem || 'reserve' in firstItem)) {
        return value;
      }
    }
  }
  
  // Если ничего не найдено, пробуем default
  if (module.default && Array.isArray(module.default) && module.default.length > 0) {
    const firstItem = module.default[0];
    if (firstItem && typeof firstItem === 'object' && ('year' in firstItem || 'value' in firstItem || 'reserve' in firstItem)) {
      return module.default;
    }
  }
  
  // Последняя попытка - ищем любой массив
  for (const key of keys) {
    const value = module[key];
    if (Array.isArray(value) && value.length > 0) {
      return value;
    }
  }
  
  console.error(`[AllData] Данные не найдены для chartId: ${chartId}, dataKey: ${dataKey}`);
  console.error(`[AllData] Доступные ключи в модуле:`, Object.keys(module));
  console.error(`[AllData] Типы значений:`, Object.keys(module).map(k => ({ key: k, type: typeof module[k], isArray: Array.isArray(module[k]) })));
  return null;
}

// Side-effect: убеждаемся, что все модули включены в сборку
// Это предотвращает tree-shaking всех импортированных модулей данных
if (import.meta.env.PROD) {
  // В production явно обращаемся к объекту, чтобы все импорты были включены
  Object.keys(allChartsDataModules).forEach(key => {
    // Доступ к модулю гарантирует его включение в бандл
    const module = allChartsDataModules[key];
    if (module && typeof module === 'object') {
      // Доступ к ключам модуля
      Object.keys(module);
    }
  });
}
